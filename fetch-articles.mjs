import fs from 'fs';

async function fetchHtml(url) {
  try {
    const response = await fetch(url);
    if (!response.ok) throw new Error(`HTTP ${response.status}`);
    return await response.text();
  } catch (err) {
    console.error(`Failed to fetch ${url}:`, err.message);
    return "";
  }
}

function extractFirstMatch(regex, html) {
  const match = regex.exec(html);
  return match ? match[1].trim() : '';
}

async function scrape() {
  console.log("Fetching index...");
  const indexHtml = await fetchHtml("https://reo.pl/pl/informacje/biznes-i-prawo");
  
  // Extract all article URLs using a regex looking for common patterns
  // Based on WordPress/CMS habits
  // Examples: href="https://reo.pl/pl/informacje/biznes-i-prawo/kim-jest-prosument-wirtualny"
  const urlRegex = /href="(https:\/\/reo\.pl\/pl\/informacje\/[^"]+)"/g;
  let match;
  const urls = new Set();
  
  while ((match = urlRegex.exec(indexHtml)) !== null) {
    const url = match[1];
    // skip tags, pagination, main categories
    if (!url.includes('/tag/') && !url.includes('?page=') && url.split('/').length > 6) {
      urls.add(url);
    }
  }
  
  const articleUrls = Array.from(urls).slice(0, 10);
  console.log(`Found ${articleUrls.length} articles! Fetching details...`);
  
  const articles = [];
  
  for (const link of articleUrls) {
    console.log(`Fetching ${link}...`);
    const articleHtml = await fetchHtml(link);
    
    // Extract Title (usually in <h1 ...>Title</h1>)
    let title = extractFirstMatch(/<h1[^>]*>([\s\S]*?)<\/h1>/, articleHtml) || extractFirstMatch(/<title>(.*?)<\/title>/, articleHtml);
    title = title.replace(/&nbsp;/g, ' ').replace(/<[^>]+>/g, '').trim().replace(/\| Reo\.pl/, '');
    
    // Extract Image (usually og:image)
    const image = extractFirstMatch(/<meta property="og:image" content="(.*?)"/, articleHtml) || 'https://images.unsplash.com/photo-1497435334941-8c899ee9e8e9?w=800&q=80';
    
    // Extract Excerpt (usually og:description)
    let excerpt = extractFirstMatch(/<meta property="og:description" content="(.*?)"/, articleHtml);
    excerpt = excerpt.replace(/&quot;/g, '"');
    
    // Extract Content (usually in a specific container, we'll try to find paragraphs)
    // For simplicity, grab all <p> inside a likely content block or just all <p> tags and join them
    // Very naive paragraph extraction:
    const paragraphRegex = /<p[^>]*>([\s\S]*?)<\/p>/g;
    let pMatch;
    const paragraphs = [];
    while ((pMatch = paragraphRegex.exec(articleHtml)) !== null) {
      const p = pMatch[1].replace(/<[^>]*>/g, '').trim();
      if (p.length > 30 && !p.includes('Copyright')) {
        paragraphs.push(p);
      }
    }
    
    const content = paragraphs.join("\n\n").replace(/&nbsp;/g, ' ').replace(/&quot;/g, '"');
    const slug = link.split('/').pop();
    
    if (title && slug) {
      articles.push({
        id: slug,
        title,
        excerpt: excerpt || content.substring(0, 120) + '...',
        slug,
        category: 'Biznes i Prawo',
        date: "2024-05-15", // placeholder, can try finding time tag
        readTime: `${Math.max(2, Math.floor(content.length / 800))} min`,
        image,
        content
      });
    }
  }
  
  const outputFile = './src/data/articles.ts';
  const tsContent = `// DO NOT EDIT - AUTOGENERATED FROM REAL REO.PL CONTENT\n\nexport interface Article {\n  id: string;\n  title: string;\n  excerpt: string;\n  slug: string;\n  category: string;\n  date: string;\n  readTime: string;\n  image: string;\n  content?: string;\n}\n\nexport const articles: Article[] = ${JSON.stringify(articles, null, 2)};\n`;
  
  fs.writeFileSync(outputFile, tsContent);
  console.log(`Saved ${articles.length} articles to ${outputFile}`);
}

scrape().catch(console.error);
